     */
    public function update(Request $request, $id)
    {
        //
        $user= User::find($id);
        $file = $request->file('image')->getClientOriginalName();
        // sampleディレクトリに画像を保存
        $request->file('image')->storeAs('', $file,'public');
        $user->name=$request->content;
        $user->image=$file;
        $user->save();
        return redirect(route('account.index'));
    }

